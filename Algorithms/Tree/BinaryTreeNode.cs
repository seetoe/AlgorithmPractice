using System;
namespace AlgorithmExercise
{
	// binary tree node
	public class BinaryTreeNode
	{
		public string value;
		public BinaryTreeNode left;
		public BinaryTreeNode right;

		public BinaryTreeNode(string value, BinaryTreeNode left, BinaryTreeNode right)
		{
			this.value = value;
			this.left = left;
			this.right = right;
		}
	}


}
